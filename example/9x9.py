# '9X9'乘法表

def main():
    # range(a, b) -> [a, b)
    print()
    print('+' + '-'*9*9 + '+')
    for i in range(1, 10, 1):
        print('|', end = '')
        for j in range(1, i + 1, 1):
            print('  %d x %d  ' %(j, i), end = '')
        print((9 - i)*9*' ' + '|')
    print('+' + '-'*9*9 + '+')


if __name__ == '__main__':
    main()

